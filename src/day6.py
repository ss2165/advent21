from math import ceil
import numpy as np
from collections import Counter


def twoD_sim(initial: list[int], days: int) -> int:
    initial_counts = Counter(initial)

    tracker = np.zeros(9, dtype=int)

    for timer, count in initial_counts.items():
        tracker[timer] = count

    for _ in range(1, days+1):
        tracker = np.roll(tracker, -1)
        tracker[6] += tracker[8]
    return sum(tracker)

def exp_sim(initial: list[int], days: int) -> int:

    fish = np.array(initial, dtype=int)

    for _ in range(days):
        ndup = fish.size - np.count_nonzero(fish)
        fish -= 1
        fish[fish == -1] = 6
        fish = np.concatenate((fish, 8*np.ones(ndup, dtype=int)))
    return len(fish)


